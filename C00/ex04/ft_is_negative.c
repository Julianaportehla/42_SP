#include <unistd.h>

void	ft_is_negative(int i)
{
	int	p;
	int	n;

	p = 78;
	n = 80;
	if (i < 0)
	{
		write(1, &p, 1);
	}
	else
	{
		write(1, &n, 1);
	}
}
